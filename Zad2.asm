	LCALL LCD_INIT 
	LJMP	START
	ORG	100H

START:
	MOV A, #01001001B 
	LCALL WRITE_HEX
	MOV A,#'H'
	LCALL WRITE_DATA 
	MOV A,#'-'
	LCALL WRITE_DATA 
	MOV A,#'>'
	LCALL WRITE_DATA 
	
	MOV A, #01001001B
	MOV B, #10
	DIV AB

	SWAP A 
	ORL A,B

	LCALL WRITE_HEX 
	MOV A,#' '
	LCALL WRITE_DATA 
	MOV A,#'B'
	LCALL WRITE_DATA 
	MOV A,#'C'
	LCALL WRITE_DATA 
	MOV A,#'D'
	LCALL WRITE_DATA 
	MOV A, #01001001B
	
STOP:				
	LJMP	STOP	
	NOP

