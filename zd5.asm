	CLR RS0
    CLR RS1
	MOV A,#10
	LCALL DELAY_100MS
	LCALL LCD_CLR
	
	MOV A, R7
	LCALL WRITE_HEX
	MOV A, #' '
	LCALL WRITE_DATA
	
	MOV A,#10
	LCALL DELAY_100MS
	LCALL LCD_CLR
	
	SETB RS0
	
	MOV A, R7
	LCALL WRITE_HEX
	MOV A, #' '
	LCALL WRITE_DATA
	
	MOV A,#10
	LCALL DELAY_100MS
	LCALL LCD_CLR
	CLR RS0
	SETB RS1
	
	MOV A, R7
	LCALL WRITE_HEX
	MOV A, #' '
	LCALL WRITE_DATA
	
	MOV A,#10
	LCALL DELAY_100MS
	LCALL LCD_CLR
	SETB RS0
	
	MOV A, R7
	LCALL WRITE_HEX
	
